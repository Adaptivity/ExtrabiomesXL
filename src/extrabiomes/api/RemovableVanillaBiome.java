package extrabiomes.api;

/**
 * Enum of vanilla biomes that can be removed from the game. All other vanilla
 * biomes are hardcoded and placed based on certain conditions.
 * 
 * @author ScottKillen
 */
public enum RemovableVanillaBiome {

	DESERT,
	EXTREMEHILLS,
	FOREST,
	PLAINS,
	TAIGA,
	JUNGLE,
	SWAMPLAND

}
